	}

	function _hex_to_rgb($hex) {
		//See http://bavotasan.com/2011/convert-hex-color-to-rgb-using-php/
		$hex = str_replace("#", "", $hex);

		if(strlen($hex) == 3) {
			$r = hexdec(substr($hex,0,1).substr($hex,0,1));
			$g = hexdec(substr($hex,1,1).substr($hex,1,1));
			$b = hexdec(substr($hex,2,1).substr($hex,2,1));
		} else {
			$r = hexdec(substr($hex,0,2));
			$g = hexdec(substr($hex,2,2));
			$b = hexdec(substr($hex,4,2));
		}

		$rgb = array($r, $g, $b);

		//return implode(",", $rgb); // returns the rgb values separated by commas
		return $rgb; // returns an array with the rgb values
	}

	function _find_closest_color($r, $g, $b){
	    $differencearray = array();

	    foreach ($this->hues as $h => $info) {
	    	$value = $this->_hex_to_rgb($h);

		    $difference = sqrt(pow($r-$value[0],2) + pow($g-$value[1],2) + pow($b-$value[2],2));
		    array_push($differencearray, $difference);
		}

	    $smallest = min($differencearray);
		$key = array_search($smallest, $differencearray);

		$k = array_keys($this->hues);
	    return $this->hues[$k[$key]]["hue"];
    }

